#include <stdio.h>
#include <stdlib.h>

int main()
{
  //Declarando inteiro = 10:
  int a = 10;

  //Declarando ponteiro pa que aponta para a:
  int *pa = &a;

  //Imprimindo os ponteiros pa e *pa:
  printf("O valor de *pa eh: %d\n", *pa);
  printf("O valor de pa eh: %d\n", (int)pa);

  //Declarando o ponteiro ppa:
  int **ppa = &pa;

  //Imprimindo o *ppa e ppa:
  printf("O valor de *ppa eh: %d\n", **ppa);
  printf("O valor de ppa eh: %d\n", (int)*ppa);
  return 0;
}
